const x=87;
const y=18;
let result;
let display_string;

//Addition
result=x+y;
display_string=`${x} + ${y} = ${result}`;
console.log(display_string);

//Substraction
result=x-y;
display_string=`${x} - ${y} = ${result}`;
console.log(display_string);

//Multiplication
result=x*y;
display_string=`${x} * ${y} = ${result}`;
console.log(display_string);

//Division
result=x/y;
display_string=`${x} / ${y} = ${result}`;
console.log(display_string);

//Remainder
result=x%y;
display_string=`${x} % ${y} = ${result}`;
console.log(display_string);